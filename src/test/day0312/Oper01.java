package test.day0312;

public class Oper01 {

	public static void main(String[] args) {
		
		
		int x = 10; 
		int y = 20;
		boolean result = x > y; 
		System.out.println(result );//비교 연산자 
		
		result = x < y; 
		System.out.println(result );//비교 연산자 
		
		result = x != y; //다르다
		System.out.println(result);// 비교연산자
		
		result = x == y; 
		System.out.println(result);// 비교연산자
		
		result = x+10 == y;
		System.out.println(result); //비교연산자
		
		
	}

}
